<p>
The University of Miami Micro'Canes team consists of five undergraduate engineering students advised by Dr. Landon Grace.  The team is working with the Multifunctional Composite Materials Lab at the University of Miami to examine how nano-particle reinforced materials are manufactured in space.</p><p>
Mark Agate, the team lead, is a senior Aerospace Engineering student, Stephen Markus is a senior Mechanical Engineering student, Felipe Gheiman is a senior Aerospace Engineering student, Nicolas Rongione is a junior Aerospace Engineering and Physics student, and Benton Patterson is a junior Mechanical and Aerospace Engineering student.  They have submitted a proposal to and been accepted by the NASA Reduced Gravity Education Flight Program and are 1 of 32 teams selected from across the across the country.  They will be flying on a modified Boeing 727 out of Houston this summer.</p><p>
	The NASA Reduced Gravity Education Flight Program gives undergraduate students the opportunity to design and build a unique experiment and fly with that experiment in a microgravity environment.  This is achieved when the aircraft dives for 20 seconds at the acceleration of gravity.  Inside the aircraft, the experiments, and students, experience a microgravity environment.</p><p>
	The Micro'Canes will be using a process that relies on cavitation (bubbles collapsing) to introduce energy into a liquid epoxy (glue) to disperse the nano-particles evenly throughout the fluid.  By being in a microgravity environment, the bubbles will not "rise" in the fluid, because there is no up or down and will thus grow larger in size and collapse with much higher energy.  This will produce a material with better physical properties.  The students are extremely excited about this opportunity as very few people ever get to experience a microgravity environment.  </p>
<br /><p>
For more information about the program, NASA Opportunities for K-12 students, or if you would like your school to be visited by the team, please contact Mark Agate at mark.a.agate@gmail.com.
